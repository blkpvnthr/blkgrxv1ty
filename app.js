let currentWeek = Number(localStorage.getItem("blkgr4v1ty-current-week") || 0);
let currentGoal = Number(localStorage.getItem("blkgr4v1ty-current-goal") || 0);

const weekNav = document.getElementById("weekNav");
const weekTitle = document.getElementById("weekTitle");
const weekTheme = document.getElementById("weekTheme");
const weekObjective = document.getElementById("weekObjective");
const goalTabs = document.getElementById("goalTabs");
const goalTitle = document.getElementById("goalTitle");
const goalWhy = document.getElementById("goalWhy");
const instructions = document.getElementById("instructions");
const checklist = document.getElementById("checklist");
const doneDefinition = document.getElementById("doneDefinition");
const goalStatus = document.getElementById("goalStatus");

const completedCount = document.getElementById("completedCount");
const totalCount = document.getElementById("totalCount");
const percentCount = document.getElementById("percentCount");
const progressFill = document.getElementById("progressFill");

document.getElementById("prevWeek").addEventListener("click", () => {
  currentWeek = Math.max(0, currentWeek - 1);
  currentGoal = 0;
  persistPosition();
  render();
});

document.getElementById("nextWeek").addEventListener("click", () => {
  currentWeek = Math.min(roadmap.length - 1, currentWeek + 1);
  currentGoal = 0;
  persistPosition();
  render();
});

function persistPosition() {
  localStorage.setItem("blkgr4v1ty-current-week", currentWeek);
  localStorage.setItem("blkgr4v1ty-current-goal", currentGoal);
}

function key(weekIndex, goalIndex, taskIndex) {
  return `w${weekIndex}-g${goalIndex}-t${taskIndex}`;
}

function getProgress() {
  return JSON.parse(localStorage.getItem("blkgr4v1ty-checks") || "{}");
}

function setProgress(progress) {
  localStorage.setItem("blkgr4v1ty-checks", JSON.stringify(progress));
}

function renderWeekNav() {
  weekNav.innerHTML = "";
  roadmap.forEach((week, i) => {
    const btn = document.createElement("button");
    btn.className = `week-btn ${i === currentWeek ? "active" : ""}`;
    btn.innerHTML = `<strong>${week.title}</strong><span>${week.theme}</span>`;
    btn.addEventListener("click", () => {
      currentWeek = i;
      currentGoal = 0;
      persistPosition();
      render();
    });
    weekNav.appendChild(btn);
  });
}

function renderGoalTabs() {
  goalTabs.innerHTML = "";
  roadmap[currentWeek].goals.forEach((goal, i) => {
    const btn = document.createElement("button");
    btn.className = `goal-tab ${i === currentGoal ? "active" : ""}`;
    btn.textContent = goal.title;
    btn.addEventListener("click", () => {
      currentGoal = i;
      persistPosition();
      render();
    });
    goalTabs.appendChild(btn);
  });
}

function renderGoal() {
  const week = roadmap[currentWeek];
  const goal = week.goals[currentGoal];

  weekTitle.textContent = week.title;
  weekTheme.textContent = week.theme;
  weekObjective.textContent = week.objective;
  goalTitle.textContent = goal.title;
  goalWhy.textContent = goal.why;
  doneDefinition.textContent = goal.done;

  instructions.innerHTML = "";
  goal.instructions.forEach(step => {
    const li = document.createElement("li");
    li.textContent = step;
    instructions.appendChild(li);
  });

  checklist.innerHTML = "";
  const progress = getProgress();
  goal.checklist.forEach((item, taskIndex) => {
    const id = key(currentWeek, currentGoal, taskIndex);
    const row = document.createElement("div");
    row.className = "task";
    row.innerHTML = `
      <input type="checkbox" id="${id}" ${progress[id] ? "checked" : ""}>
      <label for="${id}">${item}</label>
    `;
    checklist.appendChild(row);
  });

  checklist.querySelectorAll("input").forEach(box => {
    box.addEventListener("change", () => {
      const progress = getProgress();
      progress[box.id] = box.checked;
      setProgress(progress);
      updateMetrics();
      updateGoalStatus();
    });
  });

  updateGoalStatus();
}

function updateGoalStatus() {
  const goal = roadmap[currentWeek].goals[currentGoal];
  const progress = getProgress();
  const done = goal.checklist.filter((_, i) => progress[key(currentWeek, currentGoal, i)]).length;
  const pct = goal.checklist.length ? Math.round((done / goal.checklist.length) * 100) : 0;
  goalStatus.textContent = `${pct}%`;
}

function updateMetrics() {
  const progress = getProgress();
  let total = 0;
  let done = 0;

  roadmap.forEach((week, wi) => {
    week.goals.forEach((goal, gi) => {
      goal.checklist.forEach((_, ti) => {
        total++;
        if (progress[key(wi, gi, ti)]) done++;
      });
    });
  });

  const pct = total ? Math.round((done / total) * 100) : 0;
  completedCount.textContent = done;
  totalCount.textContent = total;
  percentCount.textContent = `${pct}%`;
  progressFill.style.width = `${pct}%`;
}

function render() {
  renderWeekNav();
  renderGoalTabs();
  renderGoal();
  updateMetrics();

  document.getElementById("prevWeek").disabled = currentWeek === 0;
  document.getElementById("nextWeek").disabled = currentWeek === roadmap.length - 1;
}

render();